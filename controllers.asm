UPDATECONTROLLERS:
;UPDATE EXCONTROLLER
	LDA controller1
	PHA
;LATCH CONTROLLER 1
	LDX #$01
	STX $4016
	DEX
	STX $4016
	STX controller1 ;RESET CONTROLLER1
	LDX #$08  ;SET LOOP COUNTER
CHECKCONTROLLERLOOP:
	LDA $4016
	LSR A
	ROL controller1
	DEX
	BNE CHECKCONTROLLERLOOP
ENDCHECK:
;UPDATE EDGE REGISTER
	PLA 
	EOR controller1
	STA controller1edge
	AND controller1
	STA controller1posedge
	RTS



MOVEMENT:
	LDA #$01
	CMP controller1
	BNE NOLEFTT

NOLEFTT:
	LDA #$02
	CMP controller1
	BNE NORIGHTT
NORIGHTT:
	RTS